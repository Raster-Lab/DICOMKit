STATUS_FILL = {
    "MATCH": FILL_MATCH, "OK": FILL_MATCH,
    "EXTRA_IN_STUDIO": FILL_BAD, "DRIFT": FILL_BAD, "NO_CLI_BINARY": FILL_BAD,
    "MISSING_IN_STUDIO": FILL_WARN, "INCOMPLETE": FILL_WARN,
    "NO_PARAMS_DEFINED": FILL_WARN, "MISSING_TOOL": FILL_BAD,
}


def write_header(ws, headers):
    for col, h in enumerate(headers, start=1):
        c = ws.cell(row=1, column=col, value=h)
        c.fill = FILL_HEADER
        c.font = FONT_HEADER
        c.alignment = Alignment(vertical="center", wrap_text=True)
        c.border = BORDER
    ws.freeze_panes = "A2"


def autosize(ws, widths):
    for i, w in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(i)].width = w


def build_summary_sheet(wb, summaries, cli_only):
    ws = wb.create_sheet("Summary")
    headers = ["Tool (Studio id)", "Binary", "Subcmd", "Match mode", "Network",
               "execCmd?", "Studio flags", "CLI flags", "Matched",
               "Extra in Studio", "Missing in Studio", "Parity %", "Status"]
    write_header(ws, headers)
    r = 2
    for s in summaries:
        vals = [s["tool"], s["binary"], s["subcmd"], s["mode"], s["net"], s["exec"],
                s["studio_flags"], s["cli_flags"], s["match"], s["extra_in_studio"],
                s["missing_in_studio"], s["parity_pct"], s["status"]]
        for col, v in enumerate(vals, start=1):
            cell = ws.cell(row=r, column=col, value=v)
            cell.border = BORDER
            cell.alignment = TOP
        ws.cell(row=r, column=13).fill = STATUS_FILL.get(s["status"], FILL_WARN)
        if s["extra_in_studio"]:
            ws.cell(row=r, column=10).fill = FILL_BAD
        if s["missing_in_studio"]:
            ws.cell(row=r, column=11).fill = FILL_WARN
        r += 1
    # CLI binaries with no Studio tool at all
    for binary in cli_only:
        vals = [f"(none)", binary, "", "n/a", "", "no", 0, "?", 0, 0, "?", 0.0,
                "MISSING_TOOL"]
        for col, v in enumerate(vals, start=1):
            cell = ws.cell(row=r, column=col, value=v)
            cell.border = BORDER
            cell.alignment = TOP
        ws.cell(row=r, column=13).fill = FILL_BAD
        ws.cell(row=r, column=2).fill = FILL_BAD
        r += 1
    autosize(ws, [22, 16, 12, 16, 9, 9, 11, 10, 9, 14, 16, 10, 18])


def build_detail_sheet(wb, rows):
    ws = wb.create_sheet("Input Parity")
    headers = ["Tool", "Binary", "Subcmd", "Flag", "Kind", "In CLI", "In Studio",
               "CLI default", "Studio default", "CLI help", "Studio help", "Status"]
    write_header(ws, headers)
    r = 2
    for row in rows:
        vals = [row["tool"], row["binary"], row["subcmd"], row["flag"], row["kind"],
                row["in_cli"], row["in_studio"], row["cli_default"],
                row["studio_default"], row["cli_help"], row["studio_help"], row["status"]]
        for col, v in enumerate(vals, start=1):
            cell = ws.cell(row=r, column=col, value=v)
            cell.border = BORDER
            cell.alignment = WRAP if col in (10, 11) else TOP
        ws.cell(row=r, column=12).fill = STATUS_FILL.get(row["status"], FILL_WARN)
        r += 1
    ws.auto_filter.ref = "A1:L%d" % (r - 1)
    autosize(ws, [16, 14, 10, 22, 11, 7, 9, 18, 16, 34, 34, 18])


def build_coverage_sheet(wb, cli_tools, studio_by_binary):
    ws = wb.create_sheet("Tool Coverage")
    headers = ["CLI binary", "Has Studio entry?", "Studio tool id(s)",
               "executeCommand() support?", "Subcommands", "Notes"]
    write_header(ws, headers)
    r = 2
    for binary in sorted(cli_tools.keys()):
        ids = studio_by_binary.get(binary, [])
        has = "yes" if ids else "NO"
        subs = [k for k in cli_tools[binary]["nodes"] if k]
        exec_ids = [i for i in ids if i in EXECUTE_SUPPORTED]
        exec_txt = ("yes: " + ",".join(exec_ids)) if exec_ids else ("no" if ids else "n/a")
        note = ""
        if not ids:
            note = "No CLI Workshop catalog entry — tool unreachable from the UI."
        elif not exec_ids:
            note = "Catalog entry exists but executeCommand() has no case (output not produced)."
        vals = [binary, has, ", ".join(ids), exec_txt,
                ", ".join(subs) if subs else "", note]
        for col, v in enumerate(vals, start=1):
            cell = ws.cell(row=r, column=col, value=v)
            cell.border = BORDER
            cell.alignment = WRAP if col == 6 else TOP
        ws.cell(row=r, column=2).fill = FILL_MATCH if ids else FILL_BAD
        if ids:
            ws.cell(row=r, column=4).fill = FILL_MATCH if exec_ids else FILL_WARN
        r += 1
    autosize(ws, [18, 18, 26, 30, 22, 52])


