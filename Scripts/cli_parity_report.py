# Flags injected by swift-argument-parser itself — not a Studio concern.
FRAMEWORK_FLAGS = {"--help", "-h", "--version", "--experimental-dump-help"}

# Tools whose output `CLIWorkshopViewModel.executeCommand()` actually produces
# (the `switch tool.id` cases). Everything else hits the "not yet supported"
# default branch. Source: Sources/DICOMStudio/ViewModels/CLIWorkshopViewModel.swift
EXECUTE_SUPPORTED = {
    "dicom-echo", "dicom-query", "dicom-send", "dicom-retrieve", "dicom-qr",
    "dicom-mwl", "dicom-mpps", "dicom-qido", "dicom-wado", "dicom-stow",
    "dicom-ups", "dicom-convert", "dicom-validate", "dicom-anon", "dicom-info",
    "dicom-dump", "dicom-tags", "dicom-diff",
}

# Fills
FILL_MATCH    = PatternFill("solid", fgColor="C6EFCE")  # green
FILL_BAD      = PatternFill("solid", fgColor="FFC7CE")  # red
FILL_WARN     = PatternFill("solid", fgColor="FFEB9C")  # amber
FILL_HEADER   = PatternFill("solid", fgColor="305496")  # dark blue
FILL_INFO     = PatternFill("solid", fgColor="DDEBF7")  # light blue
FONT_HEADER   = Font(color="FFFFFF", bold=True)
FONT_BOLD     = Font(bold=True)
THIN = Side(style="thin", color="D9D9D9")
BORDER = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)
WRAP = Alignment(vertical="top", wrap_text=True)
TOP = Alignment(vertical="top")


# ---------------------------------------------------------------------------
# CLI dump-help parsing (swift-argument-parser ToolInfoV0)
# ---------------------------------------------------------------------------

def arg_long_flags(arg):
    """Return the list of '--name' long flags for an argument node."""
    out = []
    for n in arg.get("names", []) or []:
        if n.get("kind") == "long":
            out.append("--" + n["name"])
        elif n.get("kind") == "longWithSingleDash":
            out.append("-" + n["name"])
    return out


def arg_short_flags(arg):
    out = []
    for n in arg.get("names", []) or []:
        if n.get("kind") == "short":
            out.append("-" + n["name"])
    return out


def normalize_arg(arg):
    """Flatten one ToolInfo argument node into a simple dict."""
    return {
        "kind": arg.get("kind"),                      # positional|option|flag
        "long": arg_long_flags(arg),
        "short": arg_short_flags(arg),
        "valueName": arg.get("valueName"),
        "isOptional": arg.get("isOptional", True),
        "isRepeating": arg.get("isRepeating", False),
        "default": arg.get("defaultValue"),
        "abstract": arg.get("abstract", ""),
        "shouldDisplay": arg.get("shouldDisplay", True),
    }


def walk_command(cmd, path):
    """
    Yield (subpath, node_args) for the root command and every subcommand.
    subpath is the space-joined subcommand path ('' for root, 'query', etc.).
    """
    args = [normalize_arg(a) for a in (cmd.get("arguments") or [])]
    yield (path, args)
    for sub in (cmd.get("subcommands") or []):
        name = sub.get("commandName", "")
        if name == "help":      # auto-generated by swift-argument-parser
            continue
        child_path = (path + " " + name).strip() if path else name
        for item in walk_command(sub, child_path):
            yield item


def load_cli_tool(filepath):
    """Load one CLI dump-help JSON -> {'binary':..., 'nodes':{subpath:[args]}}."""
    with open(filepath) as f:
        data = json.load(f)
    cmd = data.get("command", {})
    binary = cmd.get("commandName") or os.path.splitext(os.path.basename(filepath))[0]
    nodes = {}
    for subpath, args in walk_command(cmd, ""):
        nodes[subpath] = args
    return {"binary": binary, "nodes": nodes}


def option_groups(args):
    """
    Group a node's option/flag args into option records, excluding framework +
    hidden flags. Each option = the full set of its long+short tokens, so a
    Studio long-form match also covers the short alias.
    Returns (options, positionals).
      option = {tokens:set, primary:str, kind, default, abstract}
    """
    options = []
    positionals = []
    for a in args:
        if not a.get("shouldDisplay", True):
            continue
        if a["kind"] == "positional":
            positionals.append(a)
            continue
        toks = [t for t in (a["long"] + a["short"]) if t not in FRAMEWORK_FLAGS]
        if not toks:
            continue
        primary = a["long"][0] if a["long"] else toks[0]
        options.append({
            "tokens": set(toks), "primary": primary, "kind": a["kind"],
            "default": a.get("default"), "abstract": a.get("abstract", ""),
        })
    return options, positionals


def accepted_for_target(cli_tool, subpath, union_all):
    """
    Compute accepted (option_records, positionals) for a Studio tool's target.
    If union_all, merge root + ALL subcommands (lenient — used when a Studio
    tool maps to a binary root that itself has subcommands). Otherwise merge
    root args + the specific subpath node. Options are de-duplicated by primary.
    """
    nodes = cli_tool["nodes"]
    if union_all:
        sources = list(nodes.values())
    else:
        sources = []
        if "" in nodes:
            sources.append(nodes[""])          # root-level globals
        if subpath and subpath in nodes:
            sources.append(nodes[subpath])
        elif not subpath:
            sources = [nodes.get("", [])]
    by_primary = {}
    positionals = []
    for args in sources:
        opts, pos = option_groups(args)
        for o in opts:
            by_primary.setdefault(o["primary"], o)
        positionals.extend(pos)
    return list(by_primary.values()), positionals


# ---------------------------------------------------------------------------
# Studio catalog parsing
# ---------------------------------------------------------------------------

def split_name(name):
    """'dicom-wado query' -> ('dicom-wado', 'query');  'dicom-info' -> ('dicom-info','')."""
    parts = name.split(" ", 1)
    return (parts[0], parts[1].strip() if len(parts) > 1 else "")


def studio_flag_set(tool):
    """
    The flag/option TOKENS the Studio actually emits, straight from the
    introspector's `emittedFlags` (which ran buildCommand()). This is the true
    invocation contract — positional-endpoint folding (host:port), flagPicker
    (`--value`) and internal cliMapping tokens are all already applied.
    """
    return set(tool.get("emittedFlags", []))


def studio_meta_lookup(tool):
    """Best-effort flag token -> param dict, for default/help columns."""
    lut = {}
    for p in tool.get("parameters", []):
        flag = p.get("flag", "")
        if flag.startswith("-"):
            lut.setdefault(flag, p)
        if p.get("type") == "FLAG_PICKER":
            for v in p.get("allowedValues", []):
                lut.setdefault("--" + v, p)
    return lut


def studio_positionals(tool):
    return [p for p in tool.get("parameters", [])
            if p.get("isPositional") and not p.get("isInternal")]


# ---------------------------------------------------------------------------
# Comparison
# ---------------------------------------------------------------------------

def compare_tool(tool, cli_tool):
    """
    Returns (summary_dict, detail_rows[]).
    detail_rows: list of dicts with per-flag MATCH/mismatch status.
    """
    binary, subpath = split_name(tool["name"])
    union_all = (cli_tool is not None) and (subpath == "") and \
                any(k for k in cli_tool["nodes"] if k != "")
    mode = "union" if union_all else ("subcommand:" + subpath if subpath else "exact")

    s_flags = studio_flag_set(tool)
    s_lut = studio_meta_lookup(tool)
    s_pos = studio_positionals(tool)
    if cli_tool is None:
        accepted, c_pos = [], []
    else:
        accepted, c_pos = accepted_for_target(cli_tool, subpath, union_all)

    all_accepted_tokens = set()
    for o in accepted:
        all_accepted_tokens |= o["tokens"]

    rows = []
    n_match = n_extra = n_missing = 0
    matched_studio = set()

    def mkrow(flag, kind, in_cli, in_studio, cli_def, studio_def, cli_help, studio_help, status):
        return {
            "tool": tool["id"], "binary": binary, "subcmd": subpath, "mode": mode,
            "flag": flag, "kind": kind,
            "in_cli": in_cli, "in_studio": in_studio,
            "cli_default": cli_def or "", "studio_default": studio_def or "",
            "cli_help": cli_help or "", "studio_help": studio_help or "",
            "status": status,
        }

    # One row per real CLI option: is it reachable from the Studio? (a long-form
    # match also covers the option's short alias, since options compare by token set)
    for o in sorted(accepted, key=lambda x: x["primary"]):
        hit = o["tokens"] & s_flags
        if hit:
            n_match += 1; matched_studio |= hit
            p = s_lut.get(sorted(hit)[0], {})
            rows.append(mkrow(o["primary"], o["kind"], "yes", "yes",
                              o["default"], p.get("defaultValue"),
                              o["abstract"], p.get("helpText"), "MATCH"))
        else:
            n_missing += 1
            rows.append(mkrow(o["primary"], o["kind"], "yes", "no",
                              o["default"], "", o["abstract"], "", "MISSING_IN_STUDIO"))

    # Studio-emitted flags the CLI does NOT accept -> real drift (would error).
    for flag in sorted(s_flags):
        if flag in all_accepted_tokens:
            continue
        p = s_lut.get(flag, {})
        if cli_tool is None:
            status = "NO_CLI_BINARY"
        else:
            status = "EXTRA_IN_STUDIO"; n_extra += 1
        rows.append(mkrow(flag, p.get("type", ""), "no", "yes",
                          "", p.get("defaultValue"), "", p.get("helpText"), status))

    total = n_match + n_extra + n_missing
    parity = (100.0 * n_match / total) if total else (100.0 if not s_flags else 0.0)

    if cli_tool is None:
        overall = "NO_CLI_BINARY"
    elif not tool.get("parameters"):
        overall = "NO_PARAMS_DEFINED"
    elif n_extra == 0 and n_missing == 0:
        overall = "OK"
    elif n_extra > 0:
        overall = "DRIFT"      # Studio declares flags the CLI rejects -> would error
    else:
        overall = "INCOMPLETE" # Studio just doesn't expose some CLI flags

    summary = {
        "tool": tool["id"], "binary": binary, "subcmd": subpath, "mode": mode,
        "net": "yes" if tool.get("requiresNetwork") else "no",
        "exec": "yes" if tool["id"] in EXECUTE_SUPPORTED else "no",
        "studio_flags": len(s_flags), "cli_flags": len(accepted),
        "match": n_match, "extra_in_studio": n_extra, "missing_in_studio": n_missing,
        "studio_pos": len(s_pos), "cli_pos": len(c_pos),
        "parity_pct": round(parity, 1), "status": overall,
    }
    return summary, rows


