def main():
    ap = argparse.ArgumentParser(description="Generate DICOMKit CLI parity .xlsx report")
    ap.add_argument("--cli-dir", required=True, help="dir of <binary>.json dump-help files")
    ap.add_argument("--studio", required=True, help="studio-catalog.json from studio-cli-introspect")
    ap.add_argument("--out", required=True, help="output .xlsx path")
    args = ap.parse_args()

    # Load CLI dumps
    cli_tools = {}
    for fn in sorted(os.listdir(args.cli_dir)):
        if not fn.endswith(".json"):
            continue
        try:
            t = load_cli_tool(os.path.join(args.cli_dir, fn))
            cli_tools[t["binary"]] = t
        except Exception as e:
            sys.stderr.write("WARN: failed to parse %s: %s\n" % (fn, e))

    # Load Studio catalog
    with open(args.studio) as f:
        studio = json.load(f)
    studio_tools = studio.get("tools", [])

    # Map binary -> [studio tool ids]
    studio_by_binary = {}
    for t in studio_tools:
        binary, _ = split_name(t["name"])
        studio_by_binary.setdefault(binary, []).append(t["id"])

    # Compare
    summaries = []
    detail_rows = []
    for t in studio_tools:
        binary, _ = split_name(t["name"])
        cli_tool = cli_tools.get(binary)
        s, rows = compare_tool(t, cli_tool)
        summaries.append(s)
        detail_rows.extend(rows)

    # Sort: worst first (DRIFT, then MISSING/INCOMPLETE, then OK)
    order = {"DRIFT": 0, "NO_CLI_BINARY": 1, "NO_PARAMS_DEFINED": 2,
             "INCOMPLETE": 3, "OK": 4}
    summaries.sort(key=lambda s: (order.get(s["status"], 9), s["tool"]))

    cli_only = sorted(b for b in cli_tools if b not in studio_by_binary)

    # Build workbook
    wb = Workbook()
    wb.remove(wb.active)  # drop default sheet
    build_summary_sheet(wb, summaries, cli_only)
    build_detail_sheet(wb, detail_rows)
    build_coverage_sheet(wb, cli_tools, studio_by_binary)
    build_about_sheet(wb, {"cli_count": len(cli_tools), "studio_count": len(studio_tools)})

    os.makedirs(os.path.dirname(os.path.abspath(args.out)), exist_ok=True)
    wb.save(args.out)

    # Console summary
    n_drift = sum(1 for s in summaries if s["status"] == "DRIFT")
    n_incomplete = sum(1 for s in summaries if s["status"] == "INCOMPLETE")
    n_noparams = sum(1 for s in summaries if s["status"] == "NO_PARAMS_DEFINED")
    n_ok = sum(1 for s in summaries if s["status"] == "OK")
    print("CLI parity report written: %s" % args.out)
    print("  CLI binaries dumped     : %d" % len(cli_tools))
    print("  Studio catalog tools    : %d" % len(studio_tools))
    print("  OK                      : %d" % n_ok)
    print("  DRIFT (CLI rejects flag): %d" % n_drift)
    print("  INCOMPLETE (UI missing) : %d" % n_incomplete)
    print("  NO_PARAMS_DEFINED       : %d" % n_noparams)
    print("  CLI binaries w/o Studio : %d" % len(cli_only))
    if cli_only:
        print("    -> " + ", ".join(cli_only))


if __name__ == "__main__":
    main()
