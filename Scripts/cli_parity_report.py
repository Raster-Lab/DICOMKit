#!/usr/bin/env python3
"""
cli_parity_report.py  —  Tier 1 of the DICOMKit CLI parity harness.

Compares, for every DICOMKit CLI command:
  * the REAL input contract  — parsed from `<binary> --experimental-dump-help`
    JSON (swift-argument-parser ToolInfoV0), and
  * the STUDIO-declared input contract — parsed from the JSON emitted by the
    `studio-cli-introspect` executable (DICOMStudio's `ToolCatalogHelpers`).

It writes a colour-coded multi-sheet .xlsx:
  - Summary        one row per Studio tool + every CLI-only binary
  - Input Parity   one row per (tool, flag) with MATCH / mismatch status
  - Tool Coverage  one row per CLI binary (has Studio entry? executeCommand?)
  - About          legend + caveats

This is INPUT-CONTRACT parity only (Tier 1). Output parity (running the binary
vs. driving CLIWorkshopViewModel.executeCommand()) is a separate later tier.

Usage:
  python3 cli_parity_report.py --cli-dir DIR --studio FILE --out FILE.xlsx
"""

